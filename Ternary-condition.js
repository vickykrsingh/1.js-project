// 1. Write a program that takes in a number and outputs whether it is positive, negative, or zero.

// ANS.
let num = -1;

num === 0 ? console.log('The Number is Zero') : num > 0 ? console.log('The Number is Positive') : console.log('The Number is Negative')
// output : The Number is Negative


